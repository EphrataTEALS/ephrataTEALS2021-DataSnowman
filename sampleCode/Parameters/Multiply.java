public class Multiply {
    public static void main(String[] args) {
        int n = 5;
        doubleNumber(n);
        System.out.println("This is n value: " + n);
    }

    public static void doubleNumber(int x) {
        x = x * 2;
        System.out.println("This is x value: " + x);
    }
}

